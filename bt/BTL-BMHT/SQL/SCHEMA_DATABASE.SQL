    SET SLTON = SLTON + :OLD.SLGIAO
    WHERE MSMH=:OLD.MSMH;
    IF KHS_VAR = 'NO' THEN
      UPDATE TONKHODL
      SET SLTON = SLTON - :OLD.SLGIAO
      WHERE MSKH=MSKH_VAR AND MSMH=:OLD.MSMH;
    END IF;
  END IF;
EXCEPTION
  WHEN NO_DATA_FOUND THEN
    DBMS_OUTPUT.PUT_LINE('KHONG TON TAI MSPG: ' || :OLD.MSPG || ' HOAC ');
    DBMS_OUTPUT.PUT_LINE(:OLD.MSMH || ' KHONG CO TRONG DONDATHANG');
END;
