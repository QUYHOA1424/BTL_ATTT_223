--STORE PROCEDURE

CREATE OR REPLACE PROCEDURE TONG_CK_DL(MSDL VARCHAR2, THANG NUMBER)
IS
  CURSOR curMSPG
  IS
  SELECT PHIEUGIAO.MSPG,CHITIETPHIEUGIAO.MSMH,SLGIAO,DG
  FROM (KHACHHANG LEFT OUTER JOIN DONDATHANG ON KHACHHANG.MSKH=DONDATHANG.MSKH)
  LEFT OUTER JOIN
  (PHIEUGIAO LEFT OUTER JOIN CHITIETPHIEUGIAO ON PHIEUGIAO.MSPG=CHITIETPHIEUGIAO.MSPG)
  ON DONDATHANG.MSDH=PHIEUGIAO.MSDH
  WHERE KHACHHANG.MSKH=MSDL AND EXTRACT(MONTH FROM NGAYGIAO)=THANG AND EXTRACT(YEAR FROM NGAYGIAO)=EXTRACT(YEAR FROM SYSDATE);
  
  TONG_CK NUMBER:=0;
  MSKV_VAR KHACHHANG.MSKV%TYPE;
  MSLOAI_VAR MATHANG.MSLOAI%TYPE;
  MSPG_VAR CHITIETPHIEUGIAO.MSPG%TYPE;
  MSMH_VAR MATHANG.MSMH%TYPE;
  SLGIAO_VAR CHITIETPHIEUGIAO.SLGIAO%TYPE;
  DG_VAR CHITIETPHIEUGIAO.DG%TYPE;
  TYLECK_VAR CHIETKHAU.TYLECK%TYPE;
BEGIN
  SELECT MSKV INTO MSKV_VAR FROM KHACHHANG WHERE MSKH=MSDL AND KHS='NO';
  OPEN curMSPG;
  LOOP
    FETCH curMSPG INTO MSPG_VAR,MSMH_VAR,SLGIAO_VAR,DG_VAR;
    EXIT WHEN curMSPG%NOTFOUND;
    BEGIN
      SELECT MSLOAI INTO MSLOAI_VAR FROM MATHANG WHERE MSMH=MSMH_VAR;
      DBMS_OUTPUT.PUT_LINE(MSMH_VAR || ' ' || MSLOAI_VAR);
      SELECT TYLECK INTO TYLECK_VAR FROM CHIETKHAU WHERE MSKV=MSKV_VAR AND MSLOAI=MSLOAI_VAR;
      DBMS_OUTPUT.PUT_LINE(TONG_CK || ' ' || SLGIAO_VAR || ' ' || DG_VAR || ' ' || TYLECK_VAR);
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        DBMS_OUTPUT.PUT_LINE('PHIEUGIAO ' ||MSPG_VAR||' CHUA CAP NHAT CHI TIET');
        SLGIAO_VAR:=0;
        DG_VAR:=0;
        TYLECK_VAR:=0;
    END;
    TONG_CK := TONG_CK + SLGIAO_VAR*DG_VAR*TYLECK_VAR/100;
  END LOOP;
  CLOSE curMSPG;
  DBMS_OUTPUT.PUT_LINE('TONG GIA TRI CHIET KHAU CUA DAI LY ' || MSDL || ' TRONG THANG ' || THANG || ': ' || TONG_CK);
EXCEPTION
    WHEN NO_DATA_FOUND THEN
      DBMS_OUTPUT.PUT_LINE('KHONG TON TAI DAI LY ' || MSDL);
END;
/
