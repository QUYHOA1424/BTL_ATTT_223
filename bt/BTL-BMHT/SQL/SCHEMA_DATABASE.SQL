--MSDH AUTO INCREMENT TRIGGER--

CREATE SEQUENCE seq_MSDH
MINVALUE 1
MAXVALUE 9999
START WITH 1
INCREMENT BY 1
CYCLE;

CREATE OR REPLACE TRIGGER AUTO_MSDH
BEFORE INSERT
ON DONDATHANG
FOR EACH ROW
BEGIN
  :NEW.MSDH := SUBSTR(EXTRACT(YEAR FROM :NEW.NGAYDH),3,2) || LPAD(EXTRACT(MONTH FROM :NEW.NGAYDH),2,'0')
    || LPAD(EXTRACT(DAY FROM :NEW.NGAYDH),2,'0') || '/' || LPAD(seq_MSDH.NEXTVAL,4,'0');
END;
/

--TRIGGER UPDATING TONKHODL WHEN INSERT INTO CHITIETDONDATHANG--

CREATE OR REPLACE TRIGGER CTDH_INS
AFTER INSERT
ON CHITIETDONDATHANG
FOR EACH ROW
DECLARE
  MSKH_VAR DONDATHANG.MSKH%TYPE;
  SLTON_VAR TONKHODL.SLTON%TYPE;
  KHS_VAR KHACHHANG.KHS%TYPE;
BEGIN
  SELECT MSKH INTO MSKH_VAR FROM DONDATHANG WHERE MSDH=:NEW.MSDH;
  SELECT KHS INTO KHS_VAR FROM KHACHHANG WHERE MSKH=MSKH_VAR;
  IF KHS_VAR = 'NO' THEN
    BEGIN
    SELECT SLTON INTO SLTON_VAR FROM TONKHODL WHERE MSKH=MSKH_VAR AND MSMH=:NEW.MSMH;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
      INSERT INTO TONKHODL VALUES (MSKH_VAR,:NEW.MSMH,0);
      DBMS_OUTPUT.PUT_LINE('DAS');
    END;
  END IF;
EXCEPTION
   WHEN NO_DATA_FOUND THEN
      DBMS_OUTPUT.PUT_LINE('KHONG TON TAI DH: ' || :NEW.MSDH);
END;
/

