--TRIGGER CHECK NGAYGIAO--

CREATE OR REPLACE TRIGGER TRIG_CHK_NGAYGIAO
BEFORE INSERT OR UPDATE
ON PHIEUGIAO
FOR EACH ROW
DECLARE
  NGAYDH_VAR DONDATHANG.NGAYDH%TYPE;
BEGIN
  SELECT NGAYDH INTO NGAYDH_VAR FROM DONDATHANG WHERE MSDH=:NEW.MSDH;
  IF TO_DATE(:NEW.NGAYGIAO,'DD-MM-YYYY') - TO_DATE(NGAYDH_VAR,'DD-MM-YYYY') < 1 THEN
    RAISE_APPLICATION_ERROR(-20001,'NGAYGIAO PHAI SAU NGAYDH IT NHAT 1 NGAY');
  END IF;
EXCEPTION
  WHEN NO_DATA_FOUND THEN
    DBMS_OUTPUT.PUT_LINE('KHONG TON TAI MSDH: ' || :NEW.MSDH);
END;
/

--TRIGGER CHECK DONDATHANG (HOPDONG/CONGNO)--

CREATE OR REPLACE TRIGGER TRIG_CONGNO
BEFORE INSERT OR UPDATE
ON DONDATHANG
FOR EACH ROW
DECLARE
  KHS_VAR KHACHHANG.KHS%TYPE;
  CONGNO_VAR KHACHHANGSI.CONGNO%TYPE;
  DMCONGNO_VAR KHACHHANGSI.DINHMUCCONGNO%TYPE;
BEGIN
  SELECT KHS INTO KHS_VAR FROM KHACHHANG WHERE KHACHHANG.MSKH=:NEW.MSKH;
  IF KHS_VAR = 'YES' THEN
    SELECT CONGNO INTO CONGNO_VAR FROM KHACHHANGSI WHERE KHACHHANGSI.MSKH=:NEW.MSKH;
    SELECT DINHMUCCONGNO INTO DMCONGNO_VAR FROM KHACHHANGSI WHERE KHACHHANGSI.MSKH=:NEW.MSKH;
    IF CONGNO_VAR > (0.9*DMCONGNO_VAR) THEN
      RAISE_APPLICATION_ERROR(-20001,'CONG NO: ' || CONGNO_VAR || ' VUOT MUC 90% CHO PHEP');
    END IF;
  END IF;
EXCEPTION
  WHEN NO_DATA_FOUND THEN
    DBMS_OUTPUT.PUT_LINE('KHONG TON TAI MSKH: ' || :NEW.MSKH);
END;
/

--TRIGGER GIAOHANG--UPDATE SLDAGIAO--UPDATE SLTON--

CREATE OR REPLACE TRIGGER TRIG_GIAOHANG_INS
BEFORE INSERT OR UPDATE
ON CHITIETPHIEUGIAO
FOR EACH ROW
DECLARE
  MSDH_VAR DONDATHANG.MSDH%TYPE;
  SLDAT_VAR CHITIETDONDATHANG.SLDAT%TYPE;
  SLDAGIAO_VAR CHITIETDONDATHANG.SLDAGIAO%TYPE;
  SLTON_VAR MATHANG.SLTON%TYPE;
  MSKH_VAR KHACHHANG.MSKH%TYPE;
  KHS_VAR KHACHHANG.KHS%TYPE;
BEGIN
  SELECT MSDH INTO MSDH_VAR FROM PHIEUGIAO WHERE MSPG=:NEW.MSPG;
  SELECT SLDAT INTO SLDAT_VAR FROM CHITIETDONDATHANG
    WHERE MSDH=MSDH_VAR AND MSMH=:NEW.MSMH;
  SELECT SLDAGIAO INTO SLDAGIAO_VAR FROM CHITIETDONDATHANG
    WHERE MSDH=MSDH_VAR AND MSMH=:NEW.MSMH;
  SELECT SLTON INTO SLTON_VAR FROM MATHANG WHERE MSMH=:NEW.MSMH;
  SELECT MSKH INTO MSKH_VAR FROM DONDATHANG WHERE MSDH=MSDH_VAR;
  SELECT KHS INTO KHS_VAR FROM KHACHHANG WHERE MSKH=MSKH_VAR;
  IF INSERTING THEN
  IF :NEW.SLGIAO+SLDAGIAO_VAR > SLDAT_VAR THEN
    RAISE_APPLICATION_ERROR(-20002,'SO LUONG GIAO '||:NEW.MSPG||' VUOT QUA SO LUONG DAT HANG');
  ELSIF :NEW.SLGIAO > SLTON_VAR THEN
    RAISE_APPLICATION_ERROR(-20003,'SO LUONG GIAO '||:NEW.MSPG||' VUOT QUA SO LUONG TON KHO');
  ELSE 
    UPDATE CHITIETDONDATHANG
    SET SLDAGIAO = SLDAGIAO + :NEW.SLGIAO
    WHERE MSDH=MSDH_VAR AND MSMH=:NEW.MSMH;
    UPDATE MATHANG
    SET SLTON = SLTON - :NEW.SLGIAO
    WHERE MSMH=:NEW.MSMH;
    IF KHS_VAR = 'NO' THEN
      UPDATE TONKHODL
      SET SLTON = SLTON + :NEW.SLGIAO
      WHERE MSKH=MSKH_VAR AND MSMH=:NEW.MSMH;
    END IF;
  END IF;
  ELSIF UPDATING THEN
    IF SLDAGIAO_VAR + :NEW.SLGIAO - :OLD.SLGIAO > SLDAT_VAR THEN
      RAISE_APPLICATION_ERROR(-20002,'SO LUONG GIAO '||:NEW.MSPG||' VUOT QUA SO LUONG DAT HANG');
    ELSIF SLTON_VAR - :NEW.SLGIAO + :OLD.SLGIAO < 0 THEN
      RAISE_APPLICATION_ERROR(-20003,'SO LUONG GIAO '||:NEW.MSPG||' VUOT QUA SO LUONG TON KHO');
    ELSE
      UPDATE CHITIETDONDATHANG
      SET SLDAGIAO = SLDAGIAO + :NEW.SLGIAO - :OLD.SLGIAO
      WHERE MSDH=MSDH_VAR AND MSMH=:NEW.MSMH;
      UPDATE MATHANG
      SET SLTON = SLTON - :NEW.SLGIAO + :OLD.SLGIAO
      WHERE MSMH=:NEW.MSMH;
      IF KHS_VAR = 'NO' THEN
        UPDATE TONKHODL
        SET SLTON = SLTON + :NEW.SLGIAO - :OLD.SLGIAO
        WHERE MSKH=MSKH_VAR AND MSMH=:NEW.MSMH;
      END IF;
    END IF;
  END IF;
EXCEPTION
  WHEN NO_DATA_FOUND THEN
    DBMS_OUTPUT.PUT_LINE('KHONG TON TAI MSPG: ' || :NEW.MSPG || ' HOAC ');
    DBMS_OUTPUT.PUT_LINE(:NEW.MSMH || ' KHONG CO TRONG DONDATHANG');
END;
/

