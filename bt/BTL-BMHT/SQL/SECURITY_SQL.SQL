--<AUTO LABEL DATA FUNCTION>--
CREATE OR REPLACE FUNCTION ADMIN_SEC.GEN_KH_LABEL
	(V_TENKH VARCHAR2, V_KHS VARCHAR2, V_MSKV VARCHAR2)
RETURN LBACSYS.LBAC_LABEL
AS
	KH_LABEL VARCHAR2(80);
BEGIN
	IF INSTR(V_TENKH, 'VỰA THỦY SẢN') != 0 THEN
		KH_LABEL := 'TOP:';
	ELSIF INSTR(V_TENKH, 'NHÀ HÀNG') != 0 THEN
		KH_LABEL := 'MID:';
	ELSE
		KH_LABEL := 'BOT:';
	END IF;

	IF V_KHS = 'YES' THEN
		KH_LABEL := KH_LABEL || 'KS:';
	ELSIF V_KHS = 'NO' THEN
		KH_LABEL := KH_LABEL || 'DL:';
	ELSE
		KH_LABEL := KH_LABEL || ':';
	END IF;

	IF V_MSKV = 'HCM' THEN
		KH_LABEL := KH_LABEL || 'HCM';
	ELSIF V_MSKV = 'SCL' THEN
		KH_LABEL := KH_LABEL || 'SCL';
	ELSIF V_MSKV = 'DNA' THEN
		KH_LABEL := KH_LABEL || 'DNA';
	ELSE
		KH_LABEL := KH_LABEL || 'CORP';
	END IF;

	RETURN TO_LBAC_DATA_LABEL('ACCESS_KHACHHANG', KH_LABEL);
END;
/

